// ËßÜÈ¢ëÊµÅË∂ÖÊó∂Êó∂Èó¥ÔºàÊØ´ÁßíÔºâ
const VIDEO_TIMEOUT = 3000

// ËÆ°ÁÆóÂ±ûÊÄß
const displaySpeed = computed(() => {
  // ÊòæÁ§∫ÂçèËÆÆÂéüÂßãÂÄºÔºö0-1ËåÉÂõ¥Ôºå‰øùÁïô2‰ΩçÂ∞èÊï∞
  return currentSpeed.value.toFixed(2)
})

const speedAngle = computed(() => {
  // ÈÄüÂ∫¶‰ª™Ë°®ÁõòËßíÂ∫¶Ôºö0-1 ÂØπÂ∫î -135 Âà∞ 135 Â∫¶
  const maxSpeed = 1.0
  const minAngle = -135
  const maxAngle = 135
  const speed = Math.min(currentSpeed.value, maxSpeed)
  return minAngle + (speed / maxSpeed) * (maxAngle - minAngle)
})

// È¢ÑËÆ°ÁÆóÁöÑÈÄüÂ∫¶ÂàªÂ∫¶Ê†áËÆ∞ÔºàÈÅøÂÖçÈáçÂ§çËÆ°ÁÆóÔºâ
