  { value: '0.0', angle: -135 },
  { value: '0.2', angle: -81 },
  { value: '0.4', angle: -27 },
  { value: '0.6', angle: 27 },
  { value: '0.8', angle: 81 },
  { value: '1.0', angle: 135 }
]

const vehiclePosition = computed(() => {
  // Â∞ÜËΩ¶ËæÜÂùêÊ†áËΩ¨Êç¢‰∏∫Âú∞Âõæ‰∏äÁöÑÁôæÂàÜÊØî‰ΩçÁΩÆ
  // Âú∞ÂõæÂÉèÁ¥†Â∞∫ÂØ∏Ôºö1080x785ÔºåÂ∑¶‰∏ãËßí‰∏∫(0,0)ÔºåÂè≥‰∏äËßí‰∏∫(1080,785)
  // X: 0-1080 (ÂêëÂè≥‰∏∫Ê≠£), Y: 0-785 (Âêë‰∏ä‰∏∫Ê≠£)
  const mapWidth = 1080
  const mapHeight = 785
  return {
    x: (vehicleCoords.value.x / mapWidth) * 100, // XÂùêÊ†áÁôæÂàÜÊØî
    y: 100 - (vehicleCoords.value.y / mapHeight) * 100 // YÂùêÊ†áÁôæÂàÜÊØîÔºàÁøªËΩ¨ÔºåÂõ†‰∏∫CSS‰ªé‰∏äÂºÄÂßãÔºâ
  }
})

// Ê°£‰ΩçÊò†Â∞Ñ
const gearMap = {
  1: 'P',
  2: 'R', 
  3: 'N',
  4: 'D'
}

// Â§ÑÁêÜËΩ¶ËæÜ‰ø°ÊÅØÊõ¥Êñ∞‰∫ã‰ª∂
const handleVehicleInfoUpdate = (event) => {
  const vehicleInfo = event.detail
  
  // ‰ΩøÁî®Áªü‰∏ÄÁöÑËΩ¶ËæÜIDÊØîËæÉÂáΩÊï∞
  const targetId = currentVehicleId.value
  const matchesVehicleId = compareVehicleId(vehicleInfo.vehicleId, targetId)
  const matchesCarId = compareVehicleId(vehicleInfo.carId, targetId)
  
  if (!matchesVehicleId && !matchesCarId) {
    return
  }
  
  // Êõ¥Êñ∞ËøûÊé•Áä∂ÊÄÅÔºàÊï∞ÊçÆÂà∞ËææËØ¥ÊòéËøûÊé•Ê≠£Â∏∏Ôºâ
  vehicleConnected.value = true
  
  // Êõ¥Êñ∞‰ª™Ë°®ÁõòÊï∞ÊçÆÔºàÂ∏¶Êï∞ÊçÆÈ™åËØÅÔºâ
  if (typeof vehicleInfo.speed === 'number' && vehicleInfo.speed >= 0 && vehicleInfo.speed <= 1) {
    currentSpeed.value = vehicleInfo.speed
  }
  
  if (typeof vehicleInfo.steeringAngle === 'number') {
    // ÈôêÂà∂ÊñπÂêëÁõòËßíÂ∫¶ËåÉÂõ¥ -540¬∞ Âà∞ 540¬∞
    steeringAngle.value = Math.max(-540, Math.min(540, vehicleInfo.steeringAngle))
  }
  
  if (typeof vehicleInfo.battery === 'number' && vehicleInfo.battery >= 0 && vehicleInfo.battery <= 100) {
    batteryLevel.value = Math.round(vehicleInfo.battery)
  }
  
  if (vehicleInfo.gear && gearMap[vehicleInfo.gear]) {
    currentGear.value = gearMap[vehicleInfo.gear]
  }
  
  if (vehicleInfo.position && 
      typeof vehicleInfo.position.x === 'number' && 
      typeof vehicleInfo.position.y === 'number' &&
      vehicleInfo.position.x >= 0 && vehicleInfo.position.x <= 1080 &&
      vehicleInfo.position.y >= 0 && vehicleInfo.position.y <= 785) {
    vehicleCoords.value = {
      x: vehicleInfo.position.x,
      y: vehicleInfo.position.y
    }
  }
  
  console.debug(`üöó Âπ≥Ë°åÈ©æÈ©∂ÁïåÈù¢Êõ¥Êñ∞ËΩ¶ËæÜ${currentVehicleId.value}Êï∞ÊçÆ:`, {
    speed: displaySpeed.value,
    steering: steeringAngle.value,
    battery: batteryLevel.value,
    gear: currentGear.value,
    position: vehicleCoords.value
  })
}

// Â§ÑÁêÜËΩ¶ËæÜËøûÊé•Áä∂ÊÄÅÂèòÂåñ‰∫ã‰ª∂
const handleVehicleConnectionStatus = (event) => {
  const { carId, isConnected } = event.detail
  
  // ÂÆâÂÖ®ÁöÑËΩ¶ËæÜIDÂåπÈÖç
  const targetId = currentVehicleId.value
  const eventCarId = parseVehicleId(carId)
  
  if (eventCarId !== targetId) {
    return
  }
  
  console.debug(`üîó Âπ≥Ë°åÈ©æÈ©∂ÁïåÈù¢ËøûÊé•Áä∂ÊÄÅÂèòÂåñ: ËΩ¶ËæÜ${eventCarId}, ËøûÊé•:${isConnected}`)
  
  const wasConnected = vehicleConnected.value
  vehicleConnected.value = isConnected
  
  // Â¶ÇÊûú‰ªéËøûÊé•Âèò‰∏∫Êñ≠ÂºÄÔºåÈáçÁΩÆÂà∞ÂêàÁêÜÁöÑÊñ≠ÂºÄÁä∂ÊÄÅ
  if (wasConnected && !isConnected) {
    console.warn(`üöó ËΩ¶ËæÜ${currentVehicleId.value}ËøûÊé•Êñ≠ÂºÄÔºåÈáçÁΩÆÁä∂ÊÄÅ`)
    
    // Êñ≠ÂºÄÂêéÁîµÊ±†ÊòæÁ§∫0%ÔºåÊ°£‰ΩçÊòæÁ§∫P
    currentSpeed.value = 0 // ÈÄüÂ∫¶ÂΩíÈõ∂
    steeringAngle.value = 0 // ÊñπÂêëÁõòÂõûÊ≠£
    batteryLevel.value = 0 // ÁîµÊ±†ÊòæÁ§∫0%
    currentGear.value = 'P' // Ê°£‰ΩçÊòæÁ§∫P
    // ‰ΩçÁΩÆ‰øùÊåÅÊúÄÂêéÂ∑≤Áü•‰ΩçÁΩÆ
  }
}


// ÂêØÂä®ËßÜÈ¢ëÊé•Êî∂Âô®
const startVideoReceiver = async () => {
  try {
    // ÂêØÂä®UDPËßÜÈ¢ëÊúçÂä°Âô®ÔºàÂ¶ÇÊûúÂ∞öÊú™ÂêØÂä®Ôºâ
    await invoke('start_udp_video_server', { port: 8080 })
    
    // ÁõëÂê¨UDPËßÜÈ¢ëÂ∏ß‰∫ã‰ª∂
    if (!unlistenVideoFrame) {
      unlistenVideoFrame = await listen('udp-video-frame', (event) => {
        handleVideoFrame(event.payload)
      })
    }
    
    // ÂêØÂä®Â∏ßÁéáËÆ°ÁÆóÂô®
    startFrameRateCalculator()
    
    console.debug(`üìπ Âπ≥Ë°åÈ©æÈ©∂ÁïåÈù¢ÂêØÂä®ËßÜÈ¢ëÊé•Êî∂Âô®ÔºåËΩ¶ËæÜID: ${currentVehicleId.value}`)
  } catch (error) {
    try { await plError(`ÂêØÂä®UDPËßÜÈ¢ëÊé•Êî∂Âô®Â§±Ë¥•: ${error}`) } catch (_) {}
  }
}

// ÂÅúÊ≠¢ËßÜÈ¢ëÊé•Êî∂Âô®
const stopVideoReceiver = () => {
  if (unlistenVideoFrame) {
    unlistenVideoFrame()
    unlistenVideoFrame = null
  }
  
  if (frameRateTimer) {
    clearInterval(frameRateTimer)
    frameRateTimer = null
  }
  
  if (videoTimeoutTimer) {
    clearTimeout(videoTimeoutTimer)
    videoTimeoutTimer = null
  }
  
  // Ê∏ÖÁêÜblob URL
  if (videoSrc.value && videoSrc.value.startsWith('blob:')) {
    URL.revokeObjectURL(videoSrc.value)
  }
  
  videoSrc.value = ''
  lastFrameTime.value = 0
  frameRate.value = 0
  frameCount.value = 0
  cameraConnected.value = false
}

// Â§ÑÁêÜÊé•Êî∂Âà∞ÁöÑËßÜÈ¢ëÂ∏ß
const handleVideoFrame = (frame) => {
  // ÂÆâÂÖ®ÁöÑËΩ¶ËæÜIDÂåπÈÖç
  const frameVehicleId = parseVehicleId(frame.vehicle_id)
  if (frameVehicleId !== currentVehicleId.value) {
    return // ‰∏çÊòØÂΩìÂâçËΩ¶ËæÜÁöÑËßÜÈ¢ëÔºåÂøΩÁï•
  }
  
  // È™åËØÅÊï∞ÊçÆÊúâÊïàÊÄß
  if (!frame.jpeg_data || frame.jpeg_data.length === 0) {
    return
  }
  
  try {
    // È™åËØÅBase64Êï∞ÊçÆÊ†ºÂºè
    if (!/^[A-Za-z0-9+/]+=*$/.test(frame.jpeg_data)) {
      try { plWarn('UDPËßÜÈ¢ëÂ∏ßBase64Ê†°È™åÂ§±Ë¥•').catch(() => {}) } catch (_) {}
      return
    }
    
    // Êõ¥È´òÊïàÁöÑBase64Ëß£Á†Å
    const binaryString = atob(frame.jpeg_data)
    const uint8Array = Uint8Array.from(binaryString, char => char.charCodeAt(0))
    
    // È™åËØÅJPEGÊñá‰ª∂Â§¥
    if (uint8Array.length >= 2 && uint8Array[0] === 0xFF && uint8Array[1] === 0xD8) {
      // ÂàõÂª∫Blob URL
      const blob = new Blob([uint8Array], { type: 'image/jpeg' })
      
      // Ê∏ÖÁêÜ‰πãÂâçÁöÑblob URL
      if (videoSrc.value && videoSrc.value.startsWith('blob:')) {
        URL.revokeObjectURL(videoSrc.value)
      }
      
      videoSrc.value = URL.createObjectURL(blob)
      lastFrameTime.value = Date.now()
      frameCount.value++
      cameraConnected.value = true
      
      // ÈáçÁΩÆË∂ÖÊó∂Ê£ÄÊü•
      checkVideoTimeout()
    }
  } catch (error) {
    try { plError(`Â§ÑÁêÜUDPËßÜÈ¢ëÂ∏ßÂ§±Ë¥•: ${error}`).catch(() => {}) } catch (_) {}
  }
}

// ÂêØÂä®Â∏ßÁéáËÆ°ÁÆóÂô®
const startFrameRateCalculator = () => {
  if (frameRateTimer) {
    clearInterval(frameRateTimer)
  }
  
  frameRateTimer = setInterval(() => {
    const now = Date.now()
    const timeDiff = now - lastFrameCountTime.value
    
    if (timeDiff >= 1000) { // ÊØèÁßíËÆ°ÁÆó‰∏ÄÊ¨°
      frameRate.value = Math.round((frameCount.value * 1000) / timeDiff)
      frameCount.value = 0
      lastFrameCountTime.value = now
    }
  }, 1000)
}

// Ê£ÄÊü•ËßÜÈ¢ëÊµÅË∂ÖÊó∂
const checkVideoTimeout = () => {
  if (videoTimeoutTimer) {
    clearTimeout(videoTimeoutTimer)
  }
  
  videoTimeoutTimer = setTimeout(() => {
    if (videoSrc.value) {
      videoSrc.value = ''
      lastFrameTime.value = 0
      frameRate.value = 0
      cameraConnected.value = false
    }
  }, VIDEO_TIMEOUT)
}

// ÁõëÂê¨Ë∑ØÁî±ÂèòÂåñÂπ∂ÈáçÁΩÆÁä∂ÊÄÅ
watch(() => route.query.vehicleId, (newVehicleId, oldVehicleId) => {
  if (newVehicleId !== oldVehicleId) {
    const newId = parseVehicleId(newVehicleId)
    if (newId !== currentVehicleId.value) {
      currentVehicleId.value = newId
      console.debug(`üîÑ Âπ≥Ë°åÈ©æÈ©∂ÁïåÈù¢ËΩ¶ËæÜÂàáÊç¢: ${currentVehicleId.value}`)
      
      // ÈáçÁΩÆËΩ¶ËæÜÁõ∏ÂÖ≥Áä∂ÊÄÅ
      resetVehicleState()
    }
  }
})

// ÈáçÁΩÆËΩ¶ËæÜÁä∂ÊÄÅÂáΩÊï∞ÔºàÁî®‰∫éËΩ¶ËæÜÂàáÊç¢Ôºâ
const resetVehicleState = () => {
  currentSpeed.value = 0
  steeringAngle.value = 0
  batteryLevel.value = 0 // ÂàáÊç¢ËΩ¶ËæÜÊó∂ÁîµÊ±†ÊòæÁ§∫0%
  currentGear.value = 'P'
  vehicleCoords.value = { x: 540, y: 392.5 } // Âú∞Âõæ‰∏≠ÂøÉ
  
  // ÈáçÁΩÆËøûÊé•Áä∂ÊÄÅ
  vehicleConnected.value = false
  
  // ÈáçÁΩÆËßÜÈ¢ëÁä∂ÊÄÅ
  if (videoSrc.value && videoSrc.value.startsWith('blob:')) {
    URL.revokeObjectURL(videoSrc.value)
  }
  videoSrc.value = ''
  cameraConnected.value = false
  lastFrameTime.value = 0
  frameRate.value = 0
  frameCount.value = 0
}

// Âä†ËΩΩÂ∫îÁî®Ê†áÈ¢òÂíåÂàùÂßãÂåñ
onMounted(async () => {
  try {
    const res = await invoke('get_app_settings')
    if (res && res.app_title) {
      appTitle.value = res.app_title
    }
  } catch (error) {
    console.error('Âä†ËΩΩÂ∫îÁî®ËÆæÁΩÆÂ§±Ë¥•:', error)
  }
  
