const steeringAngle = ref(0) // ÊñπÂêëÁõòËßíÂ∫¶ Â∫¶
const batteryLevel = ref(85) // ÁîµÊ±†ÁîµÈáèÁôæÂàÜÊØî
const currentGear = ref('P') // ÂΩìÂâçÊ°£‰Ωç
const vehicleCoords = ref({ x: 540, y: 392.5 }) // ËΩ¶ËæÜÂùêÊ†á(ÂÉèÁ¥†)ÔºåÈªòËÆ§Âú∞Âõæ‰∏≠ÂøÉ

// ËøûÊé•Áä∂ÊÄÅÁÆ°ÁêÜ
const vehicleConnected = ref(false) // ËΩ¶ËæÜËøûÊé•Áä∂ÊÄÅ

// ÊëÑÂÉèÂ§¥Áõ∏ÂÖ≥
const videoSrc = ref('')
const cameraConnected = ref(false)
const lastFrameTime = ref(0)
